// funcao criada para exibir/esconder elementos da tela
function hideShow(campo){
	varEstado = document.getElementById(campo).style.display;

	if(varEstado == 'none'){
		document.getElementById(campo).style.display = 'block';
	} 
	if(varEstado == 'block'){
		document.getElementById(campo).style.display = 'none';
	}
}
